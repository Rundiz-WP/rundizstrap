/* 
 * Entry content limiter (read more).
 * 
 * Detect contents that has `height` size heigher than limited then collapse it, show read more text. When user click on read more then expand to show full contents.
 * 
 * @package rundizstrap
 * @since 0.0.1
 * @license http://opensource.org/licenses/MIT MIT
 */


class RundizstrapEntryContentReadmore {


    /**
     * @since 0.0.1
     * @type {String}
     */
    #collapsedClassName = 'rundizstrap-entry-content-readmore-collapsed';


    /**
     * @since 0.0.1
     * @type {String}
     */
    #expandedClassName = 'rundizstrap-entry-content-readmore-expanded';


    /**
     * @since 0.0.1
     * @type {object}
     */
    #options = {};


    /**
     * @since 0.0.1
     * @type {String} Target on HTML element that is wrap the contents. Usually it is the same with `.entry-content` that generated by WordPress core.
     */
    #readmoreContentClassName = 'rundizstrap-entry-content-readmore';


    /**
     * @since 0.0.1
     * @type {String} Read more toggler CSS class.
     */
    #readmoreTogglerClassName = 'rundizstrap-entry-content-readmore-toggler';


    /**
     * @since 0.0.1
     * @type {String} Read more wrapper CSS class for wrap a toggler button.
     */
    #readmoreWrapperClassName = 'rundizstrap-entry-content-readmore-wrapper';


    /**
     * @since 0.0.1
     * @type {String}
     */
    #shownFullClassName = 'rundizstrap-entry-content-readmore-shownfull';


    /**
     * @since 0.0.1
     * @type {Boolean}
     */
    #updatedOptions = false;


    /**
     * Class constructor.
     * 
     * @since 0.0.1
     * @param {object} options The options:<br>
     *              {string} cssSelector Class selector to select all HTML elements to activate this feature.,<br>
     *              {int} cssTransition CSS transition between collapse/expand. The value must match on your CSS but value is integer. For example: `.3s` is `300`.
     *              {int} limitHeight Limit height in pixels.
     */
    constructor(options = {}) {
        const defaultOptions = {
            'cssSelector': '.rundizstrap-entry-content-readmore',
            'cssTransition': 300,
            'limitHeight': 300,
        };

        this.#options = {...defaultOptions, ...options};

        this.#listenClickReadmore();
        this.init();
    }// constructor


    /**
     * Destroy HTML & class structure of this function.
     * 
     * @since 0.0.1
     */
    destroy() {
        document.querySelectorAll(this.#options.cssSelector)?.forEach((item) => {
            item.classList.remove(this.#collapsedClassName, this.#expandedClassName, this.#shownFullClassName);
        });

        document.querySelectorAll('.' + this.#readmoreWrapperClassName)?.forEach((item) => {
            item.remove();
        });
    }// destroy


    /**
     * Listen click on read more link/button use event delegation.
     * 
     * @since 0.0.1
     */
    #listenClickReadmore() {
        document.addEventListener('click', (event) => {
            const thisTarget = event.target;
            let readmoreBtn;
            if (thisTarget.closest('.' + this.#readmoreTogglerClassName)) {
                readmoreBtn = thisTarget.closest('.' + this.#readmoreTogglerClassName);
            } else {
                return;
            }

            event.preventDefault();

            let entryContentWrapper = readmoreBtn.parentNode?.parentNode;
            if (!entryContentWrapper || !entryContentWrapper.querySelector(this.#options.cssSelector)) {
                // if not found matched selector specified in the option.
                // do not work here.
                return;
            }// endif; 

            if (entryContentWrapper) {
                const entryContent = entryContentWrapper.querySelector(this.#options.cssSelector);
                if (entryContent) {
                    if (entryContent.classList.contains(this.#collapsedClassName)) {
                        // if entry content is currently collapsed.
                        // doing expand.
                        entryContent.classList.remove(this.#collapsedClassName);
                        entryContent.classList.add(this.#expandedClassName);

                        readmoreBtn.parentNode?.remove();
                        entryContentWrapper.insertAdjacentHTML('beforeend', this.#readlessHTML());
                    } else if (entryContent.classList.contains(this.#expandedClassName)) {
                        // if entry content is currently expanded.
                        // doing collapse.
                        entryContent.classList.remove(this.#expandedClassName);
                        entryContent.classList.add(this.#collapsedClassName);

                        readmoreBtn.parentNode?.remove();
                        entryContentWrapper.insertAdjacentHTML('beforeend', this.#readmoreHTML());

                        // scroll to nearest part of entry content on collapse (read less).
                        setTimeout(() => {
                            entryContent.scrollIntoView({block: 'nearest', behavior: 'smooth'});
                        }, (parseInt(this.#options.cssTransition) + 5));
                    }
                }// endif; entry content.
            }// endif; entry content wrapper.
        });
    }// #listenClickReadmore


    /**
     * Get read less button HTML.
     * 
     * @since 0.0.1
     * @returns {String}
     */
    #readlessHTML() {
        return '<div class="' + this.#readmoreWrapperClassName + ' ' + RundizstrapEntryContentReadmoreObj.readlessWrapperClass + '">\n\
            <a class="' + this.#readmoreTogglerClassName + ' ' + RundizstrapEntryContentReadmoreObj.readlessLinkClass + '" href="#" role="button">\n\
                ' + RundizstrapEntryContentReadmoreObj.txtReadless + '\n\
            </a>\n\
        </div>';
    }// #readlessHTML


    /**
     * Get read more button HTML.
     * 
     * @since 0.0.1
     * @returns {String}
     */
    #readmoreHTML() {
        return '<div class="' + this.#readmoreWrapperClassName + ' ' + RundizstrapEntryContentReadmoreObj.readmoreWrapperClass + '">\n\
            <a class="' + this.#readmoreTogglerClassName + ' ' + RundizstrapEntryContentReadmoreObj.readmoreLinkClass + '" href="#" role="button">\n\
                ' + RundizstrapEntryContentReadmoreObj.txtReadmore + '\n\
            </a>\n\
        </div>';
    }// #readmoreHTML


    /**
     * Initialize.
     * 
     * Search for selected selector and render required HTML/CSS class(es).
     * 
     * This method don't have to be called.<br>
     * If you use AJAX load content then you can call this method directly after displayed your contents.<br>
     * If you initialize `new` object of this class, this method will be called automatically in the `constructor()`.
     * 
     * It can be use as initialize the functional or update when there is something changed such as HTML loaded via AJAX and merged, or images have loaded.
     * 
     * @since 0.0.1
     */
    init() {
        document.querySelectorAll('.entry-content' + this.#options.cssSelector)?.forEach((item) => {
            if (
                (
                    item.classList.contains(this.#collapsedClassName) ||
                    item.classList.contains(this.#expandedClassName) ||
                    item.classList.contains(this.#shownFullClassName)
                ) &&
                false === this.#updatedOptions
            ) {
                // if the entry was initialized and options hasn't been updated.
                // skip this loop.
                return;// this is skip for `.forEach()`.
            } 
            if (true === this.#updatedOptions) {
                item.classList.remove(this.#collapsedClassName, this.#expandedClassName, this.#shownFullClassName);
                item.style.removeProperty('--entry-content-readmore-full-height');
            }// endif;

            const computedStyles = window.getComputedStyle(item);

            item.classList.add(this.#readmoreContentClassName);
            item.style.setProperty('--entry-content-readmore-full-height', computedStyles.height);

            if (parseFloat(computedStyles.height) > parseFloat(this.#options.limitHeight)) {
                // if content height is heigher than limited.
                item.classList.add(this.#collapsedClassName);

                item.parentNode.querySelector('.' + this.#readmoreWrapperClassName)?.remove();
                item.parentNode.insertAdjacentHTML('beforeend', this.#readmoreHTML());
            } else {
                item.classList.add(this.#shownFullClassName);

                item.parentNode.querySelector('.' + this.#readmoreWrapperClassName)?.remove();
            }// endif;
        });

        this.#updatedOptions = false;
    }// init


    /**
     * Re-initialize.
     * 
     * Mark updated and call to `init()` method again.
     * 
     * @since 0.0.1
     */
    reInit() {
        this.#updatedOptions = true;
        this.init();
    }// reInit


    /**
     * Update the options.
     * 
     * To re/render, you have to call `init()` method again.
     * 
     * @since 0.0.1
     * @see `RundizstrapEntryContentReadmore:constructor()`.
     * @param {object} options Read more on class constructor.
     */
    updateOptions(options = {}) {
        let currentOptions = this.#options;
        let updatedOptions = {...currentOptions, ...options};
        this.#options = updatedOptions;
        this.#updatedOptions = true;
    }// updateOptions


}// RundizstrapEntryContentReadmore


// declare variable for easy access by other JS.
var RundizstrapEntryContentReadmoreJS;


// on DOM content has loaded.
document.addEventListener('DOMContentLoaded', () => {
    RundizstrapEntryContentReadmoreJS = new RundizstrapEntryContentReadmore();
});

// on whole page has loaded. ( https://developer.mozilla.org/en-US/docs/Web/API/Window/load_event )
window.addEventListener('load', () => {
    if (RundizstrapEntryContentReadmoreJS instanceof RundizstrapEntryContentReadmore) {
        // if JS variable was set correctly.
        // update those elements again in case DOM loaded but images are not, the content's height can be shorter than when images have loaded.
        // this event listener maybe unable to detect image with lazy loading properly.
        RundizstrapEntryContentReadmoreJS.reInit();
    }
});
